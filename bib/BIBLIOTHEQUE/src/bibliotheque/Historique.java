    void historique(){
      DefaultTableModel tableModel = new DefaultTableModel(new Object[][]{}, new Object[]{"Code","Date_emprunt","Date_limite","Client","Livre","Done"});
        Gestion_Employee gest = new Gestion_Employee();
        Vector<Emprunt> list;
        try {
            list = gest.afficher_historique(client.getCin());
            for (Emprunt element : list) {

                Object[] rowData = {element.getCode(),element.getDate_emprunt(),element.getDate_limite(),element.getClient().getCin(),element.getLivre().getCode(),element.isDone()};
                tableModel.addRow(rowData);
            }
            table.setModel(tableModel);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Une erreur s'est produite : " + ex.getMessage());
        }
    }

