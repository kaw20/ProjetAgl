    private String date_match(String dateString){
String datePattern = "\\d{4}-\\d{2}-\\d{2}";
        Pattern pattern = Pattern.compile(datePattern);

        Matcher matcher = pattern.matcher(dateString);

        if (matcher.matches()) {
                return dateString ;} 
        else {
                return null;}
        }
