    boolean verif_livre(Livre livre){
    Vector<Emprunt>list=new Vector();
                Gestion_Employee gest=new Gestion_Employee();
               list =gest.afficher_tous_emprunt_effectuee();
               int count=0;
                           for (Emprunt element : list) {
                               if (element.getLivre().getTitre().equals(livre.getTitre())){
                               count++;
                               }
                           }
                           if(count>=livre.getNbr_copies()){
                           return false;}
                           else if(count==livre.getNbr_copies()-1){
                           gest.modifier_livre(livre.getCode(),0);
                           }
    return true;}
    
